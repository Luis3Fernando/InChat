    private void enviarMensajeAlServidor(String mensaje) {
        try {
            PrintWriter out = new PrintWriter(Global.conexion.getOutputStream(), true);
            out.println(mensaje);
        } catch (IOException e) {
            System.out.println("Error al enviar el mensaje al servidor: " + e.getMessage());
        }
    }

