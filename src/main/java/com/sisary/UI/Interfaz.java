    private void ButtonEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonEnviarActionPerformed
        String contenidoMensaje = InputMessage.getText();

        if (contenidoMensaje == null || contenidoMensaje.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El mensaje no puede estar vacío.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Conversacion conversacion = Global.getConversacion(Global.usuarioReceptor);
        Mensaje nuevoMensaje = new Mensaje(contenidoMensaje, Global.usuarioActual, Global.usuarioReceptor);

        conversacion.agregarMensaje(nuevoMensaje);
        Global.updateChatPanel(conversacion);

        String mensajeParaServidor = Global.usuarioActual.getNombreUsuario() + ":" + contenidoMensaje + ":" + Global.usuarioReceptor.getNombreUsuario();
        enviarMensajeAlServidor(mensajeParaServidor);

        InputMessage.setText("");
    }//GEN-LAST:event_ButtonEnviarActionPerformed

    private void ButtonCompartirArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonCompartirArchivoActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter(
                 "Archivos permitidos", "txt", "pdf", "docx", "jpg", "jpeg", "png", "gif", "mp3", "wav", "mp4", "avi", "zip", "rar");
        fileChooser.setFileFilter(filter);

        // Mostrar el diálogo de selección de archivos
        int returnValue = fileChooser.showOpenDialog(null);

        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            System.out.println("Archivo seleccionado: " + selectedFile.getAbsolutePath());
            FileClient fileClient = new FileClient("192.168.137.1", 12346, selectedFile, Global.usuarioReceptor.getNombreUsuario());

            ArchivoCliente archivoCliente = new ArchivoCliente(Global.usuarioActual.getNombreUsuario(), selectedFile.getName());

            // Agregar el archivo a la lista de archivos en Global
            Global.archivosCliente.add(archivoCliente);

            fileClient.sendFile();
            Global.updateFilesPanels();
        } else {
            System.out.println("Selección de archivo cancelada.");
        }
    }//GEN-LAST:event_ButtonCompartirArchivoActionPerformed

