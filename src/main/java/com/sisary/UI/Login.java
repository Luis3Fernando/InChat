    private void ButtonConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonConectarActionPerformed
        String nombreUsuario = this.InputUserName.getText();
        String ipServidor = this.InputDireccionIP.getText();

        if (nombreUsuario.isEmpty() || ipServidor.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            // Crear el objeto Usuario
            Usuario usuario = null;
            try {
                usuario = new Usuario(nombreUsuario, InetAddress.getLocalHost().getHostAddress());
            } catch (UnknownHostException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }

            // Guardar el usuario en la clase Global
            Global.usuarioActual = usuario;
            Global.clienteActual = new Cliente(ipServidor, 12345, usuario);

            // Verificar si la conexi칩n fue exitosa (puedes a침adir m치s validaciones si es necesario)
            if (Global.clienteActual != null && !Global.clienteActual.ExistsError()) {
                JOptionPane.showMessageDialog(this, "Conexi칩n exitosa", "Info", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                Interfaz ventana = new Interfaz();
                ventana.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Error al conectar con el servidor", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_ButtonConectarActionPerformed

